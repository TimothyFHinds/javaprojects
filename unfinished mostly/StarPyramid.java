
public class StarPyramid 
{
	public static void main(String[] args)
	{
		//Variables
		
		//print a pyramid of stars with a two nested loop
		
		//*
		//**
		//*** ...and so on
		
		for(int i = 1; i < 6; i++)
		{
			for(int j=0; j<i; j++)
				System.out.print("*");
			System.out.println();
			
			
		}
		
		
		
	}
}
